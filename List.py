
# Creating an empty list
List = []
print("An empty list")
print(List)

# Creating a list holding a string
List = ["GeeksforGeekss"]
print("\n A list with the use of a string")
print(List)

# Creating a list with multiple strings
List = ["Geeks", "For", "Geeks"]
print("\n Lists holding specific indexes")
print(List[0])
print(List[2])

# Creating a multi-dimensional list
List = [["Geeks", "For"], "Geek"]
print("\n A multi-dimensional list")
print(List)
