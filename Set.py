
# Creating an empty set
set1 = set()
print("Empty set")
print(set1)

# Creating a set with a string
set1 = set("geeksforgeeks")
print("\n A set holding a string")
print(set1)

# Creating a set with a list
set1 = set(["geeks","for","geeks"])
print("\n A set holding a list")
print(set1)

# Creating a set with mixed values
set1 = set([1,2,"Geeks",4,"For",6,"Geeks"])
print("\n A set holding a list of mixed values")
print(set1)
